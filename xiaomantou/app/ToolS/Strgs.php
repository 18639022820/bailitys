<?php

namespace App\ToolS;

class Strgs
{ 
   	static function randomstr($num=6){
   		for($i=0;$i<$num;$i++){
			$chi[]= chr(rand(97, 122));
			$chi[]= chr(rand(65, 90));
			$chi[]= chr(rand(48, 57));		
		}
		$str="";
		foreach ($chi as $key => $value) {
			$str.=$value;		
		}
		return $str;
   	}
   	
}
